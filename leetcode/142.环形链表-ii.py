# Definition for singly-linked list.
class ListNode:
    def __init__(self, x):
        self.val = x
        self.next = None

class Solution:
    def detectCycle(self, head: ListNode) -> ListNode:
        fast = head  
        slow = head
        while True:
            if not (fast and fast.next): 
                return
            fast = fast.next.next
            slow = slow.next
            if fast == slow: 
                break
        fast = head
        while fast != slow: 
            #第二次相遇会在环形链表入口相遇，第一次相遇在b处
            #第二次，各自行走a，慢指针a+b
            fast = fast.next
            slow = slow.next
        return fast



        
